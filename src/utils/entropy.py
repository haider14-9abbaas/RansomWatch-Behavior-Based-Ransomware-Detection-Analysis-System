import math

def shannon_entropy_bytes(data: bytes) -> float:
    """Return Shannon entropy (0..8) for byte data."""
    if not data:
        return 0.0
    freq = [0] * 256
    for b in data:
        freq[b] += 1
    n = len(data)
    ent = 0.0
    for c in freq:
        if c:
            p = c / n
            ent -= p * math.log2(p)
    return ent
